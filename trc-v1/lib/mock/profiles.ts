// Helper function to get a random mock profile
export function getRandomMockProfile() {
  return mockProfiles[Math.floor(Math.random() * mockProfiles.length)];
}

// Helper function to get a mock profile by ID
export function getMockProfileById(id: string) {
  return mockProfiles.find((profile) => profile.id === id) || mockProfiles[0];
}

// Helper function to get multiple mock profiles
export function getMockProfiles(count: number = 10) {
  const profiles = [];
  for (let i = 0; i < count; i++) {
    profiles.push(mockProfiles[i % mockProfiles.length]);
  }
  return profiles;
}
